import UIKit

public extension UIColor {
    class var DH_FirstWordColor: UIColor {
        get {return UIColor.init(hexString: "#333333")}
    }
    class var DH_SecondWordColor: UIColor {
        get {return UIColor.init(hexString: "#999999")}
    }
    class var DH_BackgroundColor: UIColor {
        get {return UIColor.init(hexString: "#F2F2F2")}
    }
    class var DH_Red: UIColor {
        get {return UIColor.init(hexString: "#F14143")}
    }
    class var DH_Yellow: UIColor {
        get {return UIColor.init(hexString: "#F19715")}
    }
    class var DH_Green: UIColor {
        get {return UIColor.init(hexString: "#39AF6F")}
    }
    class var DH_ThreadFirst: UIColor {
        get {return UIColor.init(hexString: "#E6E6E6")}
    }
    class var DH_ThreadSecond: UIColor {
        get {return UIColor.init(hexString: "#DFDFDF")}
    }
}
